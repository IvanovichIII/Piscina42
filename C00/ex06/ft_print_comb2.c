#include <unistd.h>

void	ft_print_comb2(void)
{
	char	n[5];
	int		a;
	int		b;

	a = 0;
	n[2] = ' ';
	while (a < 99)
	{
		while (b < 100)
		{
			if (b > a)
			{
				n[0] = (a / 10) + '0';
				n[1] = (a % 10) + '0';
				n[3] = (b / 10) + '0';
				n[4] = (b % 10) + '0';
				write (1, &n, 5);
				if (a != 98)
					write (1, ", ", 2);
			}
			b ++;
		}
		a ++;
		b = 0;
	}
}
