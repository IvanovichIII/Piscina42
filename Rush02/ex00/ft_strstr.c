#include <stddef.h>

char	*ft_strstr(char *s1, char *s2)
{
	char	*or_s1;
	char	*or_s2;

	or_s1 = s1;
	or_s2 = s2;
	while (*s1 != '\0')
	{
		or_s1 = s1;
		s2 = or_s2;
		while (*s1 != '\0' && *s2 != '\0' && *s1 == *s2)
		{
			s1++;
			s2++;
		}
		if (*s2 == '\0')
			return (or_s1);
		s1++;
	}
	return (NULL);
}
